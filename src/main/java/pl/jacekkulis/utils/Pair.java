package pl.jacekkulis.utils;

public class Pair {
    private Integer i;
    private Double d;

    public Pair(Integer i, Double d) {
        this.i = i;
        this.d = d;
    }

    public Integer getI() {
        return i;
    }

    public void setI(Integer i) {
        this.i = i;
    }

    public Double getD() {
        return d;
    }

    public void setD(Double d) {
        this.d = d;
    }
}
